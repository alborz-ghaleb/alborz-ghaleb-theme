/* تشخیص زبان جاری سایت (Polylang یا locale وردپرس)
 * [PERF v5.0.5] cached per-request
 */
function glass_current_lang() {
    static $lang_cache = null;
    if ( null !== $lang_cache ) {
        return $lang_cache;
    }
    if ( function_exists( 'pll_current_language' ) ) {
        $l = pll_current_language( 'slug' );
        if ( ! empty( $l ) ) {
            $lang_cache = $l;
            return $lang_cache;
        }
    }
    $loc = strtolower( substr( get_locale(), 0, 2 ) );
    $lang_cache = $loc ? $loc : 'fa';
    return $lang_cache;
}

/* آیا زبان جاری راست‌به‌چپ است؟ [PERF: cached] */
function glass_lang_is_rtl() {
    static $rtl_cache = null;
    if ( null !== $rtl_cache ) {
        return $rtl_cache;
    }
    $rtl_cache = in_array( glass_current_lang(), [ 'fa', 'ar', 'he', 'ur' ], true );
    return $rtl_cache;
}

/* مترجم متمرکز — کلید را به متن زبان جاری ترجمه می‌کند
 * [PRO] فیلتر «glass_t» اجازه می‌دهد افزونه/ماژول (مثل Polylang) خروجی را override کند.
 * [PERF v5.0.5] cache per-request — جلوگیری از reconstruction آرایه ترجمه‌ها در هر صدا
 */
function glass_t( $key, $lang = null ) {
    static $translate_cache = [];
    $lang = $lang ?: glass_current_lang();
    $cache_key = $lang . '|' . $key;
    if ( isset( $translate_cache[ $cache_key ] ) ) {
        return $translate_cache[ $cache_key ];
    }
    $S = glass_ads_strings();

    if ( isset( $S[ $lang ][ $key ] ) ) {
        $value = $S[ $lang ][ $key ];
    } elseif ( isset( $S['en'][ $key ] ) ) {
        $value = $S['en'][ $key ]; // fallback انگلیسی
    } else {
        $value = $key;
    }

    /**
     * امکان override ترجمه (مثلاً از Polylang Strings).
     *
     * @param string $value متن نهایی.
     * @param string $key   کلید رشته.
     * @param string $lang  زبان جاری.
     */
    $result = apply_filters( 'glass_t', $value, $key, $lang );
    $translate_cache[ $cache_key ] = $result;
    return $result;
}

